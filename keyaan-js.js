
function switchStatement(a) {
    switch (a) {
        case 1:
            console.log("option 1");
            break;
        case 2:
            console.log("option 2");
            break;
        case 3:
            console.log("option 3");
            break;
        default:
            console.log("IDK")
    }
}

switchStatement(1)