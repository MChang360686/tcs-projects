class node {
    int value;
    node next, prev;

    public node(int value) {
        this.value = value;
        this.next = null;
        this.prev = null;
    }
}

public class doublylinkedlist {
    node head, tail;

    public doublylinkedlist() {
        this.head = null;
        this.tail = null;
    }

    
}
